using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace WhatIsInterface
{
    public class ObjectOrientedProgramming
    {
        /**
         * c#은 객체 지향 프로그래밍이라는 캡슐화, 상속, 다형성, 추상화 등 개념을 제공
         * 
         * 객체 지향 프로그래밍(Object Oriented Programming) = OOP
         * 현재까지 현업에서 가장 많이 사용하는 프로그램 작성 기법
         * 
         * 목적
         *  - 프로그램 분석 용이
         *  - 프로그램 유지보수 용이
         *  - 특정 기능 재사용 가능
         *  
         * 학술적인 의미로 지향되는 것이지 만능은 아님.
         * 
         * Tip!
         *  - C#에서 클래스와 객체는 명사로 표현, 속성은 명사 또는 형용사, 메서드는 동사의 의미가 있다.
         *  
         * 개체
         * 클래스를 사용하여 새로운 형식을 정의
         * 데이터와 기능을 숨기는 캡슐화(Encapsulation) 기능을 제공
         * 
         *  - (is something) 무엇이 된다.
         *  - (has data) 데이터를 가진다.
         *  - (performs action) 기능을 수행한다.
         * 
         * 객체 지향 프로그래밍의 네 가지 큰 개념
         *  - 추상화(Abstraction) : 설계 관련 개념
         *      구성은 정해져 있으나 구성물의 내용이 정해져 있지 않음
         *  
         *  - 캡슐화(Encapsulation) : 캡슐처럼 내용을 숨기는 것
         *      필드는 특별한 경우가 아니면 private로 클래스 안에서만 사용
         *      구조화된 데이터 개체 값 또는 상태를 숨겨 권한이 없으면 접근하지 못하도록 차단
         *      연관된 자료 구조와 메서드를 한 테두리로 묶는 것
         *      원칙 - 필드는 private 외부 공개는 public으로 속성 사용
         *      
         *  - 상속(Inheritance) : 부모 재산을 자식에게 상속하여 능력을 주는 것
         *      부모 클래스의 멤버를 자식 클래스에서 재사용 하는 개념
         *      
         *  - 다형성(Polymorphism) : 다양한 형태를 가질 수 있는 능력
         *      특정 클래스의 메서드는 매개변수에 따라 여러가지 다양한 형태를 가진다.
         *      (메서드 오버로딩!)
         */
    }
}
